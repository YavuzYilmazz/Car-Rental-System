package RentACar;

public class Date {
		private int day;
		private int month;
		private int year;
		
		public Date(int inputDay, int inputMonth, int inputYear) {
			day = inputDay;
			month = inputMonth;
			year = inputYear;}
		
		
		public int getDay() {return day;}
		public int getMonth() {return month;}
		public int getYear() {return year;}
		
		
		public void setDay(int inputDay) {day = inputDay;}
		public void setMonth(int inputMonth) {month = inputMonth;}
		public void setYear(int inputYear) {year = inputYear;}
	}
    

